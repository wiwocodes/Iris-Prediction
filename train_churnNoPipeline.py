import pandas as pd
import mlflow
import mlflow.sklearn
from sklearn.ensemble import RandomForestClassifier

def train(x_train_enc,y_train):
    mlflow.set_experiment("Streamlit-ChurnPipeline")


    with mlflow.start_run() as run:
        model_churnNoPipeline = RandomForestClassifier(criterion= 'gini',max_depth=4)
        model_churnNoPipeline.fit(x_train_enc, y_train)

        mlflow.log_param("criterion", "gini")
        mlflow.log_param("max_depth", 4)
        mlflow.sklearn.log_model(model_churnNoPipeline,artifact_path="model") 
        return run.info.run_id  #A run_id is just a string generated by MLflow.

if __name__ == "__main__":
    train()

